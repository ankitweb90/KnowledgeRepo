package ExamplesMapReduce;

public class Employee {

	private String name;
	private int salary;
	private int age;
	
	public Employee(String name, int salary, int age) {
		super();
		this.name = name;
		this.salary = salary;
		this.age = age;
	}
	

	public String getName() {
		return name;
	}

	public int getSalary() {
		return salary;
	}

	public int getAge() {
		return age;
	}
	
	
}
